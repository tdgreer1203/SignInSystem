    private void checkTime() {
        Date date = new Date();
        Calendar c = Calendar.getInstance();
        c.setTime(date);
        int t = c.get(Calendar.HOUR_OF_DAY) * 100;
        if((t >= mornFrom && t <= mornTo) || (t >= nightFrom && t <= nightTo)) {
            db.closeAllSessions();
        }
    }
        
