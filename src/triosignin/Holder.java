    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        if(db.isInSystem(jTextField1.getText())) {
            if(db.hasOpenSession(jTextField1.getText())){
                db.endSession(db.getStudentID(jTextField1.getText()));
                JOptionPane.showMessageDialog(this, "You have been signed out.", "Signed Out", JOptionPane.PLAIN_MESSAGE);
                jTextField1.setText("");
            }
            else {
                db.startSession(db.getStudentID(jTextField1.getText()));
                JOptionPane.showMessageDialog(this, "You are now signed in.", "Signed In", JOptionPane.PLAIN_MESSAGE);
                jTextField1.setText("");
            }           
        }
        else {
            JOptionPane.showMessageDialog(this, "You must register.", "Please Register", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            deactivateMain();
            rm.setVisible(true);
            
            rm.addWindowListener(new WindowAdapter(){
            @Override
            public void windowClosing(WindowEvent e) {
                activateMain();
            }
            });
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

